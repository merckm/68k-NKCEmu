/**
 * Emulates the Bankboot card to enable booting with memory at address 0
 */

#include <stdio.h>
#include <stdbool.h>
#include "bankboot.h"
#include "log.h"

bankboot g_bb;

BYTE_68K bank_pC8_in()
{
  return 0;
}

void bank_pC8_out(BYTE_68K data)
{
  log_debug("Disable Bankboot");
  g_bb.bb_enabled = FALSE;
}

void bank_reset()
{
  log_debug("Reset, Bankboot enabled");
  g_bb.bb_enabled = TRUE;
}
